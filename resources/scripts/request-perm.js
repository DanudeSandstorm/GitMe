function toggle1A() {
    var a = document.getElementById('firstLineA');
    var b = document.getElementById('firstLineR');
    var r = document.getElementById('l1b');
    var t = document.getElementById('l1at');
    a.style.display = 'none';
    b.style.display = 'none';
    r.style.backgroundColor = '#00ff00'
    t.className = "show";
}

